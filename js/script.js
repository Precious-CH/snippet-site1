const countrySelect = document.getElementById("countrySelect");
const languageSelect = document.getElementById("languageSelect");

countrySelect.addEventListener("change", () => {
    const selectedCountry = countrySelect.value;
    languageSelect.innerHTML = '<option value="">-- Select Language --</option>';
    
    if (selectedCountry && languagesByCountry[selectedCountry]) {
    const langs = languagesByCountry[selectedCountry];
    langs.forEach(lang => {
        const option = document.createElement('option');
        option.value = lang.toLowerCase();
        option.textContent = lang;
        languageSelect.appendChild(option);
    });
    languageSelect.disabled = false;
    } else {
    languageSelect.disabled = true;
    }
    
    if (!selectedCountry) {
    translatePage('en');
    }
});

languageSelect.addEventListener("change", () => {
    const selectedLang = languageSelect.value;
    if (selectedLang) {
    const langCode = langCodeMap[selectedLang] || "en";
    translatePage(langCode);
    }
});

function translatePage(langCode) {
    const elements = document.querySelectorAll('[data-translate]');
    const targetTranslations = translations[langCode] || translations.en;
    
    elements.forEach(el => {
    const key = el.getAttribute('data-translate');
    if (targetTranslations[key]) {
        el.style.opacity = '0.7';
        setTimeout(() => {
        el.textContent = targetTranslations[key];
        el.style.opacity = '1';
        }, 100);
    }
    });
    
    if (langCode === 'ar') {
    document.body.setAttribute('dir', 'rtl');
    } else {
    document.body.setAttribute('dir', 'ltr');
    }
}

